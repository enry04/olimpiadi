class RecordsManager {
    constructor(tableElement) {
        this.rootElement = tableElement;
        this.headerValues = ["Nome fase", "Punteggio", "Nome", "Cognome", "Data di nascita"];
        this.tHead = this.rootElement.createTHead();
        this.tBody = this.rootElement.createTBody();
    }

    init() {
        this.initElements();
    }

    initElements() {
        let row = this.tHead.insertRow();
        for (let i = 0; i < this.headerValues.length; i++) {
            let th = document.createElement("th");
            th.innerHTML = this.headerValues[i];
            row.appendChild(th);
        }
    }

    setRowData(phase, points, name, surname, born) {
        let data = [phase, points, name, surname, born];
        let row = this.tBody.insertRow();
        for (let i = 0; i < this.headerValues.length; i++) {
            let td = document.createElement("td");
            td.innerHTML = data[i];
            row.appendChild(td);
        }
    }
}

export default RecordsManager;